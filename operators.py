#COMPARISION OPERATOR
x=3
y=5
print(x==y)
print(x!=y)
print(x>y)
print(x<y)
print(x>=y)
print(x<=y)
#LOGICAL OPERATOR
print(x > 3 and x < 10)
print(x > 3 or x < 10)
print(not(x > 3 and x < 10))
#IDENTITY OPERATOR
x=5
y=10
print(x is y)
print(x == y)
print(x is not y)
print(x != y)
#MEMBERSHIP OPERATORS
